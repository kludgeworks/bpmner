import fs from 'node:fs';
import path from 'node:path';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const rulesDir = path.join(repoRoot, 'rules');
const manifestPath = path.join(repoRoot, 'src', 'rule-manifest.ts');
const docsDir = path.join(repoRoot, 'docs', 'rules');
const defaultOutputPath = path.join(repoRoot, 'src', 'generated', 'static-rules.ts');
const cliOutputPath = process.argv[2];
const bazelPackageOutputRoot =
  process.env.BAZEL_BINDIR && process.env.BAZEL_PACKAGE
    ? path.join(process.env.BAZEL_BINDIR, process.env.BAZEL_PACKAGE)
    : null;
const execRoot =
  bazelPackageOutputRoot && repoRoot.endsWith(bazelPackageOutputRoot)
    ? repoRoot.slice(0, -bazelPackageOutputRoot.length - 1)
    : path.dirname(repoRoot);
const isBazelOutputPath = (value) => value.startsWith('bazel-out/') || value.startsWith('bazel-out\\');
const outputPath = !cliOutputPath
  ? defaultOutputPath
  : path.isAbsolute(cliOutputPath)
    ? cliOutputPath
    : isBazelOutputPath(cliOutputPath)
      ? path.resolve(execRoot, cliOutputPath)
      : path.resolve(process.cwd(), cliOutputPath);

const recommendedConfig = require('bpmnlint/config/recommended');

const manifestSource = fs.readFileSync(manifestPath, 'utf8');

const manifestEntries = [...manifestSource.matchAll(/\{\s*id:\s*'([^']+)',\s*level:\s*'(warn|error)'\s*\}/g)].map(
  ([, id, level]) => ({ id, level })
);

const customRuleFiles = fs
  .readdirSync(rulesDir)
  .filter((file) => file.endsWith('.ts') && !file.startsWith('_'))
  .sort();

const customRuleIds = customRuleFiles.map((file) => file.replace(/\.ts$/, ''));
const manifestIds = manifestEntries.map(({ id }) => id).sort();
const docFiles = fs
  .readdirSync(docsDir)
  .filter((file) => file.endsWith('.md') && file !== 'README.md')
  .sort();
const docRuleIds = docFiles.map((file) => file.replace(/\.md$/, ''));

if (JSON.stringify(customRuleIds) !== JSON.stringify(manifestIds)) {
  throw new Error(
    `Rule manifest mismatch.\nFiles: ${customRuleIds.join(', ')}\nManifest: ${manifestIds.join(', ')}`
  );
}

if (JSON.stringify(docRuleIds) !== JSON.stringify(manifestIds)) {
  throw new Error(
    `Rule docs mismatch.\nDocs: ${docRuleIds.join(', ')}\nManifest: ${manifestIds.join(', ')}`
  );
}

const builtinRules = Object.entries(recommendedConfig.rules)
  .map(([id, level]) => ({ id, level }))
  .sort((a, b) => a.id.localeCompare(b.id));

const toIdentifier = (value) =>
  value
    .replace(/(^|[-_/])([a-z])/g, (_, __, ch) => ch.toUpperCase())
    .replace(/[^A-Za-z0-9]/g, '')
    .replace(/^[0-9]/, '_$&')
    .replace(/^([A-Z])/, (match) => match.toLowerCase());

const builtinImports = builtinRules
  .map(({ id }) => `import ${toIdentifier(id)} from 'bpmnlint/rules/${id}';`)
  .join('\n');

const customImports = manifestEntries
  .map(({ id }) => `import ${toIdentifier(id)} from '../../rules/${id}';`)
  .join('\n');

const builtinRuleEntries = builtinRules
  .map(({ id }) => `  '${id}': ${toIdentifier(id)},\n  'bpmnlint/${id}': ${toIdentifier(id)},`)
  .join('\n');

const customRuleEntries = manifestEntries
  .map(({ id }) => `  'bpmner/${id}': ${toIdentifier(id)},\n  'bpmnlint-plugin-bpmner/${id}': ${toIdentifier(id)},`)
  .join('\n');

const coreRecommendedConfigEntries = builtinRules.map(({ id, level }) => `    '${id}': '${level}',`).join('\n');
const coreRecommendedErrorConfigEntries = builtinRules.map(({ id }) => `    '${id}': 'error',`).join('\n');
const customRecommendedConfigEntries = manifestEntries
  .map(({ id, level }) => `    '${id}': '${level}',`)
  .join('\n');
const customAllConfigEntries = manifestEntries
  .map(({ id }) => `    '${id}': 'error',`)
  .join('\n');
const customRuleDocsEntries = manifestEntries
  .map(({ id }) => {
    const content = fs.readFileSync(path.join(docsDir, `${id}.md`), 'utf8');
    return [
      `  '${id}': ${JSON.stringify(content)},`,
      `  'bpmner/${id}': ${JSON.stringify(content)},`,
      `  'bpmnlint-plugin-bpmner/${id}': ${JSON.stringify(content)},`,
    ].join('\n');
  })
  .join('\n');

const source = `// Generated by linter/scripts/generate-static-rules.mjs. Do not edit by hand.
${builtinImports}

${customImports}

export const rules: Record<string, unknown> = {
${builtinRuleEntries}
${customRuleEntries}
};

export const configs = {
  'bpmnlint:recommended': {
    rules: {
${coreRecommendedConfigEntries}
    },
  },
  'plugin:bpmner/recommended': {
    rules: {
${customRecommendedConfigEntries}
    },
  },
  'plugin:bpmner/all': {
    rules: {
${customAllConfigEntries}
    },
  },
  'plugin:bpmnlint-plugin-bpmner/recommended': {
    rules: {
${customRecommendedConfigEntries}
    },
  },
  'plugin:bpmnlint-plugin-bpmner/all': {
    rules: {
${customAllConfigEntries}
    },
  },
  'bpmnlint:recommended-error': {
    rules: {
${coreRecommendedErrorConfigEntries}
    },
  },
  'plugin:bpmner/recommended-error': {
    rules: {
${coreRecommendedErrorConfigEntries}
${customRecommendedConfigEntries}
    },
  },
  'plugin:bpmnlint-plugin-bpmner/recommended-error': {
    rules: {
${coreRecommendedErrorConfigEntries}
${customRecommendedConfigEntries}
    },
  },
};

export const customRuleDocs: Record<string, string> = {
${customRuleDocsEntries}
};

export const resolver = {
  resolveRule(pkg: string, name: string) {
    const combinations = [
      pkg ? \`\${pkg}/\${name}\` : name,
      name,
      \`bpmnlint/\${name}\`,
      \`bpmner/\${name}\`,
      \`bpmnlint-plugin-bpmner/\${name}\`,
    ];

    for (const key of combinations) {
      const rule = rules[key];
      if (rule) {
        return typeof rule === 'object' && rule && 'default' in rule ? (rule as { default: unknown }).default : rule;
      }
    }

    return null;
  },
  resolveConfig(pkg: string, name: string) {
    const configKey =
      pkg === 'bpmnlint'
        ? \`bpmnlint:\${name}\`
        : pkg
          ? \`plugin:\${pkg}/\${name}\`
          : name;
    const config = configs[configKey as keyof typeof configs];
    if (config) {
      return config;
    }

    throw new Error(\`Config resolution not supported in static resolver: \${pkg}/\${name}\`);
  },
  resolveRuleDoc(name: string) {
    return customRuleDocs[name as keyof typeof customRuleDocs] || null;
  },
};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, source);
